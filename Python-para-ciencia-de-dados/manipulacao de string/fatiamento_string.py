# Start, Stop e Step
nome = "Guilherme Arthur de Carvalho"

print(nome[0])
print(nome[-1])
print(nome[:9]) # termina no nono
print(nome[10:]) # começa no décimo
print(nome[10:16]) # começa no 10 e termina com 16
print(nome[10:16:2]) # de dois em dois
print(nome[:]) 
print(nome[::-1]) # espelha string (de trás pra frente)

